"""Q12. Write a Python program that takes two numbers as input and
performs the following operations: addition, subtraction, multiplication,
division, and modulus. Display the results."""
n1 = int(input("Enter a first number="))
n2 = int(input("Enter a second number="))
sum = n1+n2
sub = n1-n2
prod = n1*n2
div = n1/n2
mod = n1 % n2
print("Sum of two number=", sum)
print("Subtraction of two number=", sub)
print("Multiplication of two number=", prod)
print("Division of two number=", div)
print("Modulus of two number=", mod)
