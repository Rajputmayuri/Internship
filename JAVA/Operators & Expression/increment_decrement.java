//Write a program to demonstrate the use of pre-increment, post-increment,pre-decrement, and post-decrement operators and display the results.

package com.company;

public class increment_decrement {
    static void main() {
        int a=10;
        System.out.println("Pre-increment :" +(++a));
        System.out.println("Post-increment :" +(a++));
        System.out.println("Pre-decrement :" +(--a));
        System.out.println("Post-decrement :" +(a--));

    }
}
